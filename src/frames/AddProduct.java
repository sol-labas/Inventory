    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jName.getText();
        Double price = 0.0;
        Integer quantity = 0;
        
        try {
            price = Double.parseDouble(jPrice.getText());
        } catch (NumberFormatException e) {
            e.printStackTrace();
        }
        try {
            quantity = Integer.parseInt(jQuantity.getText());
        } catch (NumberFormatException e) {
            e.printStackTrace();
        }
        if (name == "" || quantity <= 0 || price <=0.0){
            jErrorMsg.setText("Please enter correct product name, quantitity and price");
            return;
        }
        ProductManagement productMgmt = new ProductManagement(Execution.conn);
        Product product = productMgmt.addProduct(name);
        if (product == null) {
            jErrorMsg.setText("Cannot create product. Perhaps it already exists?");
            return;
        }
        productMgmt.changeProduct(product, quantity, price * quantity, new Date(), Execution.loggedUser);
        this.setVisible(false);
        new ControlPanel().setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

